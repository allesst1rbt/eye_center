  <h3>ATENÇÃO AOS CUIDADOS COM SUA LENTE:</h3>

  <ol>
    <li>
      <strong>Higiene das Mãos:</strong>
      <ul>
        <li>Lave e seque bem as mãos antes de manusear as lentes.</li>
      </ul>
    </li>

    <li>
      <strong>Colocação das Lentes:</strong>
      <ul>
        <li>Verifique se a lente está limpa e sem danos.</li>
        <li>Coloque a lente no dedo indicador, puxe as pálpebras e coloque-a sobre o olho.</li>
        <li>Pisque suavemente para ajustar a lente.</li>
      </ul>
    </li>

    <li>
      <strong>Remoção das Lentes:</strong>
      <ul>
        <li>Lave as mãos antes de remover as lentes.</li>
        <li>Puxe a pálpebra inferior e use o polegar e o indicador para pinçar e retirar a lente.</li>
      </ul>
    </li>

    <li>
      <strong>Armazenamento:</strong>
      <ul>
        <li>
          Para lentes <strong>GELATINOSAS</strong> (coloridas e incolores, mensais e anuais): use sempre solução recomendada para armazenamento. Indicamos o uso da solução Renu.
        </li>
        <li>
          Para lentes <strong>RÍGIDAS E ESCLERAIS</strong>: use sempre solução recomendada para armazenamento. Indicamos o uso diário da solução Boston Simplus.
        </li>
        <li>Troque a solução do estojo a cada remoção das lentes.</li>
      </ul>
    </li>

    <li>
      <strong>Tempo de Uso:</strong>
      <ul>
        <li>Seguir as orientações do fabricante.</li>
        <li>Respeite o prazo de validade das suas lentes, para evitar infecções.</li>
      </ul>
    </li>

    <li>
      <strong>Cuidados Adicionais:</strong>
      <ul>
        <li>Evite dormir com lentes (a menos que orientado e discutido com seu médico).</li>
        <li>Não use lentes danificadas ou mal higienizadas.</li>
        <li>Evite nadar com as lentes.</li>
      </ul>
    </li>

    <li>
      <strong>Sinais de Alerta:</strong>
      <ul>
        <li>Remova as lentes imediatamente e consulte-nos se você sentir:</li>
        <ul>
          <li>Dor, vermelhidão ou irritação nos olhos.</li>
          <li>Visão embaçada ou halos ao redor das luzes.</li>
          <li>Sensibilidade à luz.</li>
          <li>Secreção ou lacrimejamento excessivo.</li>
        </ul>
      </ul>
    </li>

    <li>
      <strong>Consultas Regulares:</strong>
      <ul>
        <li>Agende consultas oftalmológicas regulares para monitorar a saúde dos seus olhos e garantir que suas lentes de contato continuem adequadas para você.</li>
        <li>A frequência das consultas será definida pelo seu oftalmologista.</li>
      </ul>
    </li>
  </ol>

